    /// <summary>Returns the single-character glyph string for the given icon
    /// name, or an empty string if the name is unknown.</summary>
    public static string GetGlyph(string? iconName)
    {
        if (string.IsNullOrEmpty(iconName)) return string.Empty;
        return Codepoints.TryGetValue(iconName, out var cp) ? char.ConvertFromUtf32(cp) : string.Empty;
    }

    public static bool IsKnown(string? iconName) =>
        !string.IsNullOrEmpty(iconName) && Codepoints.ContainsKey(iconName);
